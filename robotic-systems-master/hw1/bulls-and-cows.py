def bulls_and_cows():
    """
    –õ–æ–≥–∏—á–µ—Å–∫–∞—è –∏–≥—Ä–∞ "–ë—ã–∫–∏ –∏ –∫–æ—Ä–æ–≤—ã".
    –ö–æ–º–ø—å—é—Ç–µ—Ä –∑–∞–≥–∞–¥—ã–≤–∞–µ—Ç 4-–∑–Ω–∞—á–Ω–æ–µ —á–∏—Å–ª–æ –±–µ–∑ –ø–æ–≤—Ç–æ—Ä—è—é—â–∏—Ö—Å—è —Ü–∏—Ñ—Ä.
    –ò–≥—Ä–æ–∫ –ø—ã—Ç–∞–µ—Ç—Å—è —É–≥–∞–¥–∞—Ç—å –∑–∞ 10 –ø–æ–ø—ã—Ç–æ–∫.
    """
    import random

    # –ì–µ–Ω–µ—Ä–∞—Ü–∏—è —á–∏—Å–ª–∞
    digits = list("0123456789")
    random.shuffle(digits)
    secret = "".join(digits[:4])

    print("–Ø –∑–∞–≥–∞–¥–∞–ª 4-–∑–Ω–∞—á–Ω–æ–µ —á–∏—Å–ª–æ –±–µ–∑ –ø–æ–≤—Ç–æ—Ä—è—é—â–∏—Ö—Å—è —Ü–∏—Ñ—Ä.")
    print("–ü–æ–ø—Ä–æ–±—É–π —É–≥–∞–¥–∞—Ç—å! (10 –ø–æ–ø—ã—Ç–æ–∫)")

    for attempt in range(1, 11):
        guess = input(f"–ü–æ–ø—ã—Ç–∫–∞ {attempt}: ").strip()

        if not (guess.isdigit() and len(guess) == 4):
            print("–û—à–∏–±–∫–∞: –≤–≤–µ–¥–∏—Ç–µ 4-–∑–Ω–∞—á–Ω–æ–µ —á–∏—Å–ª–æ.")
            continue

        # –ü–æ–¥—Å—á—ë—Ç –±—ã–∫–æ–≤ –∏ –∫–æ—Ä–æ–≤
        bulls = sum(secret[i] == guess[i] for i in range(4))
        cows = sum(ch in secret for ch in guess) - bulls

        print(f"–ë—ã–∫–∏: {bulls}, –ö–æ—Ä–æ–≤—ã: {cows}")

        if bulls == 4:
            print(f"üéâ –ü–æ–∑–¥—Ä–∞–≤–ª—è–µ–º! –í—ã —É–≥–∞–¥–∞–ª–∏ —á–∏—Å–ª–æ {secret} –∑–∞ {attempt} –ø–æ–ø—ã—Ç–æ–∫!")
            return

    print(f"–ò–≥—Ä–∞ –æ–∫–æ–Ω—á–µ–Ω–∞. –ó–∞–≥–∞–¥–∞–Ω–Ω–æ–µ —á–∏—Å–ª–æ –±—ã–ª–æ: {secret}")


# –í—ã–∑–æ–≤ —Ñ—É–Ω–∫—Ü–∏–∏
bulls_and_cows()
